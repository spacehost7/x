from llm_and_x import generate_with_claude, post_to_x, now_jst


def main() -> None:
    now = now_jst()
    date_str = now.strftime("%Y-%m-%d（%a）")

    prompt = f"""
あなたは日本の株式市場とマクロ環境に詳しいアナリストです。
日本時間の大引け後に、Xに投稿する1ポスト分の文章を日本語で必ず1本だけ作成してください。

重要ルール:
- 実際の終値データの有無にかかわらず、必ずポスト文を生成してください。
- データがないことを理由に断ったり、謝罪文や注意喚起を書いてはいけません。
- 一般的によくある値動きや要因を、それっぽいフィクションとして書いてください。

条件:
- 1行目は日付だけにする（例: {date_str}）
- 2行目以降で、1日の相場のざっくりした振り返り（上げ/下げ要因、主なセクター）を一般論レベルでまとめる
- 全体で140文字以内を目安にする
- やや砕けた口調にする
- ハッシュタグは最大2個まで
- 絵文字は使わない
- 個別銘柄名や具体的な指数・数値は出さない

出力フォーマット:
- 不要な前置きや説明は一切書かず、Xに投稿する本文だけを出力してください。
"""

    text = generate_with_claude(prompt)
    post_to_x(text)


if __name__ == "__main__":
    main()
